import UIKit

extension UIFont {
    static func MontserratBold(size: CGFloat) -> UIFont{
        return UIFont(name: "Montserrat-Bold", size: size)!
    }
    
    static func MontserratLight(size: CGFloat) -> UIFont{
        return UIFont(name: "Montserrat-Light", size: size)!
    }
    
  
}